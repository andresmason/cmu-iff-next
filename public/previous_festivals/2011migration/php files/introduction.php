The fictional and documentary films presented in Faces of Migration gives life to the questions surrounding the issues of migration.  What drives migration?  What are the ramifications for both the native and host country? How is an immigrant’s identity both enriched and plagued by the meaning of nationality, citizenship and belonging, capitalism, and cultural difference?  By offering a glimpse into the extraordinary lives and humanity of a marginalized population, the films allow us to advance the debate surrounding the urgent issue of modern migration.
<br>
<br>
Five years since its inception, the Carnegie Mellon International Film Festival continues to celebrate the fine art of filmmaking and the themes which define our contemporary landscape.  We have searched the world to bring you the several films featured in this year’s Faces of Migration (- selecting features from China, France, Germany, Greece, India,  Israel, Korea, Mexico,  Netherlands, Poland, Romania, Turkey and our very own Pittsburgh).  These films cannot be delivered straight to your door, nor seen in commercial theaters or streamed on your computer screen. From top films at Cannes, Berlin, and Sundance to cutting-edge documentaries, many of these projects are still in festival circuit or vying for cinemas most prestigious recognitions. Moreover, each screening is an incredible event featuring panels with renowned international directors or casts, live performances ranging from opera singing to break dancing, and controversies that will undoubtedly rouse heated protest.<br>
<br>
